package Class6;

public class E7SwitchStatement {
    public static void main(String[] args) {
        String name="Elvis";
        switch (name){
            case "Justin":
                System.out.println("Manchester");
                break;
            case "Jerry":
                System.out.println("london");
                break;
            case "Solomon":
                System.out.println("Texas");
                break;
            case "Charles":
                System.out.println("Lagos");
                break;
            case "Elvis":
                System.out.println("Aberdeen");
                break;
            case "Emeka":
                System.out.println("Maryland");
                break;
            case "Tina":
                System.out.println("Sapele");
                break;
            case "Amaka":
                System.out.println("Bolton");
                break;
            default:
                System.out.println("Not available");
        }
    }
}
