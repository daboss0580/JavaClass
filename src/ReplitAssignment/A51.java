package ReplitAssignment;

public class A51 {
    public static void main(String[] args) {
        /*
        Using do while loop print numbers from 10 to 20

Must not include the number 20 in the output
         */
        int start = 10;
        do {
            System.out.println(start);
            start++;
        }while (start<20);
    }
}
