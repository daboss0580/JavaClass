package ReplitAssignment;

public class A48 {
    public static void main(String[] args) {
        //Using while loops that print out even numbers from 1 to 13
        int start = 1;
        while (start <= 13) {
            if (start % 2 == 0) {
                System.out.println(start);

            }
            start++;
        }
    }
}
