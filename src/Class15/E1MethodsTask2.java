package Class15;

public class E1MethodsTask2 {
    static void numbers(int x){
        if (x%2==0){
            System.out.println("The number "+x+" is an even number");
        }else System.out.println("The number "+x+" is an odd number");
    }

    public static void main(String[] args) {
        numbers(4);
    }
}
