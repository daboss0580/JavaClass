package Class8;

public class E13NestedLoops {
    public static void main(String[] args) {
        for (int a = 0; a < 5; a++) {
            int b = 2;
            while (b <= 10) {
                System.out.print(b+ " ");
                b+=2;

            }
            System.out.println();
        }
    }
}