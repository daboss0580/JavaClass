public class E10NestedLoops {
    public static void main(String[] args) {
        for (int n1 = 0; n1 < 5; n1++) {
            System.out.print("$ ");
        }
        System.out.println();
        for (int n1 = 0; n1 < 5; n1++) {
            System.out.print("$ ");
        }
        System.out.println();
        for (int n1 = 0; n1 < 5; n1++) {
            System.out.print("$ ");

        }
    }
}