package class3;

public class DoubleComparison {
    public static void main(String[] args) {
        double price1 = 17.30;
        double price2 = 12.40;
        if (price1 < price2) {
            System.out.println("Double value " + price1 + " is larger than " + price2);
        } else {
            System.out.println("Double value " + price1 + "is less than " + price2);
        }
    }
}
