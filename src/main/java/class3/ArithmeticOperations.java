package class3;

public class ArithmeticOperations {
    public static void main(String[] args) {
        double num1=20.0;
        double num2=15.0;
        System.out.println(num1+num2);
        System.out.println(num1-num2);
        System.out.println(num1*num2);
        System.out.println(num1/num2);
    }
}
